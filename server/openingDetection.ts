/**
 * Detect the ECO opening from a list of SAN moves.
 * Returns the most specific (longest) matching opening.
 *
 * @param moves - Array of SAN move strings (e.g. ["e4", "e5", "Nf3", "Nc6"])
 * @returns OpeningInfo or null if no match found
 */
export function detectOpening(moves: string[]): OpeningInfo | null {
  if (!moves || moves.length === 0) return null;

  const gamePrefix = moves.join(" ");
  let bestMatch: OpeningInfo | null = null;
  let bestMatchLength = 0;

  for (const [eco, name, variation, openingMoves] of ECO_TABLE) {
    if (!openingMoves) {
      // Empty moves = catch-all for the first move
      if (moves.length > 0 && bestMatchLength === 0) {
        bestMatch = { eco, name, variation };
        bestMatchLength = 0;
      }
      continue;
    }

    // Check if the game starts with this opening's moves
    if (gamePrefix.startsWith(openingMoves) || openingMoves === gamePrefix) {
      if (openingMoves.length > bestMatchLength) {
        bestMatch = { eco, name, variation };
        bestMatchLength = openingMoves.length;
      }
    }
  }

  return bestMatch;
}

/**
 * Format the opening info into a display string.
 * e.g. "Sicilian Defense: Najdorf Variation (B90)"
 */
export function formatOpeningName(info: OpeningInfo): string {
  const base = info.variation ? `${info.name}: ${info.variation}` : info.name;
  return `${base} (${info.eco})`;
}
